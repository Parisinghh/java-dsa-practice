public class DataTypes {
    public static void main(String[] args) {

        // Primitive data types
        int a = 10;              // integer
        float b = 5.5f;          // decimal
        double c = 10.99;        // decimal 
        char d = 'J';            // single character
        boolean e = true;        // true/false
        long f = 100000L;        // large integer
        byte g = 120;            // small integer
        short h = 32000;         // short integer

        // Non-primitive data type
        String name = "Java";

        // Printing values
        System.out.println("int: " + a);
        System.out.println("float: " + b);
        System.out.println("double: " + c);
        System.out.println("char: " + d);
        System.out.println("boolean: " + e);
        System.out.println("long: " + f);
        System.out.println("byte: " + g);
        System.out.println("short: " + h);
        System.out.println("String: " + name);
    }
}
